// Mode 2: OpenAPI spec → entire API surface
// ─────────────────────────────────────────────────────────────────────
// When the API has a published OpenAPI spec, you can generate hundreds
// of typed Swift models in one command. Run it once, commit the
// generated wrappers, treat them as source.

// Workflow:
//
//   1. Get the spec (curl from the API provider, or grab a copy from
//      Stripe / GitHub / your own backend team).
//
//        $ curl https://api.stripe.com/openapi.json > stripe-spec.json
//
//   2. Run the CLI:
//
//        $ swift run smartapi-bundle openapi stripe-spec.json Sources/MyApp/Generated/
//        generated Sources/MyApp/Generated/Customer+SmartAPI.swift
//        generated Sources/MyApp/Generated/Charge+SmartAPI.swift
//        generated Sources/MyApp/Generated/PaymentIntent+SmartAPI.swift
//        ... 200+ more
//        smartapi-bundle openapi: 203 schemas imported
//
//   3. Commit the generated files. They're plain Swift; diffing
//      them in PRs shows exactly what changed when the API evolves.
//
//   4. Use them like any other @SmartAPI type:

