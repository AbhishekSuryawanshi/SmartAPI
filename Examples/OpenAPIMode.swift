// MARK: - Why this mode

// • **Scale**: 200+ schemas in one command. Hand-writing those Codables
//   would take a team a week.
//
// • **Diff-on-PR**: when the API adds a field, the next CLI run
//   shows up as a Swift diff. Reviewers see exactly what changed.
//
// • **Single source of truth**: the .smartapi.json wrappers ARE your
//   contract with the API. No drift between OpenAPI doc and code.
//
// • **Idempotent**: re-running the CLI on an unchanged spec does
//   nothing. Safe to wire into CI.
