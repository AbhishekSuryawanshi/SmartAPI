// MARK: - Catalog the endpoints (this part you still write)

enum StripeAPI {
    static let client = SmartClient(
        defaultHeaders: ["Stripe-Version": "2024-04-10"],
        baseURL: URL(string: "https://api.stripe.com/v1")!,
        authorization: BearerTokenProvider(initialToken: ProcessInfo.processInfo.environment["STRIPE_KEY"]) {
            // For Stripe, the API key doesn't refresh — return nil to
            // give up rather than retry-loop.
            return nil
        },
        retryPolicy: .standard
    )

    // The CLI generates types named after the schema in components.schemas.
    // Here we reference them in a typed endpoint catalog.
    //
    //   static let listCustomers = SmartEndpoint<[Customer.Model]>(path: "/customers")
    //   static let getCustomer   = SmartEndpoint<Customer.Model>(path: "/customers/{id}")
    //   static let getCharge     = SmartEndpoint<Charge.Model>(path: "/charges/{id}")
}

